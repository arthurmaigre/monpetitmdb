// Grand Paris = tous les departements 75, 92, 93, 94
const DEPT_GRAND_PARIS = ['75', '92', '93', '94']

async function fetchCSV() {
  return new Promise((resolve, reject) => {
    const url = 'https://www.data.gouv.fr/api/1/datasets/r/008a2dda-2c60-4b63-b910-998f6f818089'
    https.get(url, { headers: { 'Accept': 'text/csv' } }, (res) => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        https.get(res.headers.location, (res2) => {
          let data = ''
          res2.on('data', chunk => data += chunk)
          res2.on('end', () => resolve(data))
          res2.on('error', reject)
        })
        return
      }
      let data = ''
      res.on('data', chunk => data += chunk)
      res.on('end', () => resolve(data))
      res.on('error', reject)
    })
  })
}

function findMetropole(codeInsee) {
  // Grand Paris : departements 75, 92, 93, 94
  const dept = codeInsee.substring(0, 2)
  if (DEPT_GRAND_PARIS.includes(dept)) return 'Grand Paris'

  for (const [metropole, codes] of Object.entries(METROPOLES)) {
    if (metropole === 'Grand Paris') continue
    if (codes.includes(codeInsee)) return metropole
  }
  return null
}

async function main() {
  console.log('Telechargement du referentiel codes postaux...')
  const csv = await fetchCSV()
  const lines = csv.split('\n').filter(l => l.trim())
  console.log(`${lines.length - 1} lignes trouvees`)

  // Parse CSV (separateur ;)
  const header = lines[0].split(';')
  console.log('Colonnes:', header)

  const rows = []
  for (let i = 1; i < lines.length; i++) {
    const cols = lines[i].split(';')
    if (cols.length < 4) continue
    const codeInsee = cols[0].trim()
    const nomCommune = cols[1].trim()
    const codePostal = cols[2].trim()
    const libelleAcheminement = cols[3].trim()

    if (!codePostal || codePostal.length !== 5) continue

    const metropole = findMetropole(codeInsee)
    rows.push({
      code_insee: codeInsee,
      nom_commune: nomCommune,
      code_postal: codePostal,
      libelle_acheminement: libelleAcheminement,
      metropole,
    })
  }

  console.log(`${rows.length} communes parsees`)
  const withMetro = rows.filter(r => r.metropole)
  console.log(`${withMetro.length} communes dans une metropole`)

  // Stats par metropole
  const stats = {}
  for (const r of withMetro) {
    stats[r.metropole] = (stats[r.metropole] || 0) + 1
  }
  console.log('\nCommunes par metropole:')
  for (const [m, c] of Object.entries(stats).sort((a, b) => b[1] - a[1])) {
    console.log(`  ${m}: ${c}`)
  }

  // Vider la table existante
  console.log('\nSuppression des anciennes donnees...')
  const { error: delError } = await sb.from('ref_communes').delete().neq('id', 0)
  if (delError) console.log('Erreur suppression (table vide ?):', delError.message)

  // Insert par batches de 500
  console.log(`\nInsertion de ${rows.length} communes...`)
  const BATCH = 500
  let inserted = 0
  for (let i = 0; i < rows.length; i += BATCH) {
    const batch = rows.slice(i, i + BATCH)
    const { error } = await sb.from('ref_communes').insert(batch)
    if (error) {
      console.log(`Erreur batch ${i}-${i + batch.length}:`, error.message)
    } else {
      inserted += batch.length
      process.stdout.write(`\r  ${inserted}/${rows.length}`)
    }
  }

  console.log(`\n\nTermine ! ${inserted} communes inserees.`)
}

main().catch(console.error)
